    private void Book_listActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Book_listActionPerformed
        // TODO add your handling code here:
        BName=Book_list.getSelectedItem().toString();
        String[][] info_book=DB_Connect.getBookInfo();
            for(int i=0;i<info_book.length;i++){
                if(info_book[i][2].equals(BName)){
                    BGID=Integer.parseInt(info_book[i][0]);
                    break;
                }
            }
            PID=DB_Connect.getPublisherID(BGID);
            String[] info=DB_Connect.getPublisherInfo(PID);
            Publisher_id.setText(info[0]);
            Publisher_name.setText(info[1]);
            Publisher_address.setText(info[2]);
            Publisher_contact.setText(info[3]);
            Publisher_email.setText(info[4]);
    }//GEN-LAST:event_Book_listActionPerformed
