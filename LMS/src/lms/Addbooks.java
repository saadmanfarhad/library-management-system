    private void Button_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_AddActionPerformed
        // TODO add your handling code here:
        boolean b=true;
        while(b==true){
            Edition=Edition_val.getText();
            copies=Integer.parseInt(Copies_val.getText());
            DB_Connect.AddBook(PID,BID,BGID,copies,BName,AName,Edition);
          
            int x=JOptionPane.showConfirmDialog(null,"Do you want to add more books?","Add Books",2,JOptionPane.QUESTION_MESSAGE);
            
            if(x==0){
                b=true;
                Edition_val.setText(null);
                Copies_val.setText(null);
            }
            else {
                b=false;
                if(window==1){
                    this.dispose();
                    Home_Admin home_admin=new Home_Admin();
                    home_admin.setVisible(true);
                }
                else {
                    this.dispose();
                    Viewbooks view=new Viewbooks();
                    view.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_Button_AddActionPerformed

